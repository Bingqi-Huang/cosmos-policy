#!/usr/bin/env bash
set -euo pipefail

# SCVC sanity ladder entrypoint.
# Default mode is CPU-only contract/report generation.  GPU rungs are guarded by
# RUN_GPU=1 so this script can be used safely while another training job owns GPUs.

export BASE_DATASETS_DIR="${BASE_DATASETS_DIR:-.}"
PAIR_MANIFEST_PATH="${PAIR_MANIFEST_PATH:-outputs/phase2/pair_future_frames/libero_pair_future_manifest_train.jsonl}"
PAIR_ROLLOUT_DATA_DIR="${PAIR_ROLLOUT_DATA_DIR:-LIBERO-Cosmos-Policy/all_episodes}"
OUT_DIR="${OUT_DIR:-outputs/phase3/scvc_sanity}"

mkdir -p "${OUT_DIR}"

CONTRACT_ARGS=(
  --manifest "${PAIR_MANIFEST_PATH}"
  --repo-root "."
  --output-json "${OUT_DIR}/contract.json"
)

if [[ "${CHECK_IMAGES:-0}" == "1" ]]; then
  CONTRACT_ARGS+=(--check-images)
fi
if [[ "${DATASET_SAMPLE:-0}" == "1" ]]; then
  CONTRACT_ARGS+=(
    --dataset-sample
    --data-dir "LIBERO-Cosmos-Policy/success_only"
    --rollout-data-dir "${PAIR_ROLLOUT_DATA_DIR}"
    --t5-text-embeddings-path "LIBERO-Cosmos-Policy/success_only/t5_embeddings.pkl"
  )
fi

uv run --extra cu128 --group libero --python 3.10 \
  python cosmos_policy/experiments/robot/libero/check_scvc_batch_contract.py "${CONTRACT_ARGS[@]}"

if [[ "${RUN_GPU:-0}" != "1" ]]; then
  cat > "${OUT_DIR}/gpu_ladder.json" <<'JSON'
{
  "status": "pending",
  "reason": "RUN_GPU was not set to 1; GPU rungs intentionally skipped.",
  "required_rungs": [
    "lambda_cv=0 fixed-batch equivalence",
    "bs10 K_s=2 memory smoke",
    "100-pair overfit",
    "A1 derangement quick run",
    "A2 invariant_plus_fscene quick shrinkage check"
  ]
}
JSON
else
  echo "RUN_GPU=1 requested.  Launch GPU rungs from the experiment registry; this harness only records the gate."
  cat > "${OUT_DIR}/gpu_ladder.json" <<'JSON'
{
  "status": "skipped",
  "reason": "GPU rung commands are generated by generate_phase3_launchers.py and must be launched intentionally."
}
JSON
fi

uv run --extra cu128 --group libero --python 3.10 \
  python cosmos_policy/experiments/robot/libero/generate_scvc_sanity_report.py \
  --contract-json "${OUT_DIR}/contract.json" \
  --gpu-json "${OUT_DIR}/gpu_ladder.json" \
  --output-md "${OUT_DIR}/summary.md"

echo "[sanity] wrote ${OUT_DIR}/summary.md"
